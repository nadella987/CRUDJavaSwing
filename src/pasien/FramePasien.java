    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        try {
            pst = db.con.prepareStatement("insert into pasien (nik, nama_pasien, tanggal_lahir, no_telp, alamat) VALUES (?,?,?,?,?)");
            pst.setString(1, txtNik.getText());
            pst.setString(2, txtNamaPasien.getText());
            pst.setString(3, txtTanggalLahir.getText());
            pst.setString(4, txtNoTelp.getText());
            pst.setString(5, txtAlamat1.getText());
            pst.executeUpdate(); 
            loadData();
            JOptionPane.showMessageDialog(null, "Data pasien tersimpan");
            pst.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_btnTambahActionPerformed


    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
        int row = tblPasien.getSelectedRow();
        // Mengambil nilai id dari kolom pertama (indeks 0)
        String id = tblPasien.getValueAt(row, 0).toString(); 

        pst = db.con.prepareStatement("UPDATE pasien SET nik=?, nama_pasien=?, tanggal_lahir=?, no_telp=?, alamat=? WHERE id=?");
        pst.setString(1, txtNik.getText());
        pst.setString(2, txtNamaPasien.getText());
        pst.setString(3, txtTanggalLahir.getText());
        pst.setString(4, txtNoTelp.getText());
        pst.setString(5, txtAlamat1.getText());
        pst.setString(6, id); // Menggunakan nilai id dari baris yang dipilih
        pst.executeUpdate();
        loadData();
        JOptionPane.showMessageDialog(null, "Data pasien tersimpan");
        pst.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

